# Vamos a practicar if y condiciones:

if 2 < 4:
    print("2 es menor que 4")

# Esto es muy simple, qué otras condiciones podemos utilizar?:

# Si queremos evaluar que 2 variables son iguales:
# a == b

# Si una variable es menor a otra:
# a < b

# Si es mayor a otra:
# a > b

# Si es distinta a la otra:
# a != b

# Si es mayor o igual:
# a >= b

# Si es menor o igual:
# a <= b

if 2 == 2:
    print("2 es igual a 2")

if 2 > 1:
    print("2 es mayor a 1")

if 2 != 3:
    print("2 es distinto de 3")

